-- STEP 8: CREATE STORED PROCEDURE - CAPTURE STREAM HEALTH (FIXED - NO RESULT_SCAN)
-- =============================================================================
CREATE OR REPLACE PROCEDURE D_BRONZE.CDC_MONITORING.SP_CAPTURE_STREAM_HEALTH()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS CALLER
AS
$$
DECLARE
    v_result VARCHAR;
    v_snapshot_tms TIMESTAMP_NTZ := CURRENT_TIMESTAMP();
    v_table_name VARCHAR;
    v_stream_name VARCHAR;
    v_is_stale BOOLEAN;
    v_stale_after TIMESTAMP_NTZ;
    v_hours_until_stale NUMBER;
    v_has_pending_data BOOLEAN;
    v_stream_status VARCHAR;
    v_count NUMBER := 0;
    
    c1 CURSOR FOR 
        SELECT TABLE_NAME, STREAM_NAME 
        FROM D_BRONZE.CDC_MONITORING.CDC_PIPELINE_CONFIG 
        WHERE IS_ACTIVE = TRUE;
BEGIN
    OPEN c1;
    FOR rec IN c1 DO
        v_table_name := rec.TABLE_NAME;
        v_stream_name := rec.STREAM_NAME;
        
        BEGIN
            SELECT 
                STALE,
                STALE_AFTER,
                TIMESTAMPDIFF(HOUR, CURRENT_TIMESTAMP(), STALE_AFTER)
            INTO v_is_stale, v_stale_after, v_hours_until_stale
            FROM D_RAW.INFORMATION_SCHEMA.STREAMS
            WHERE STREAM_CATALOG = 'D_RAW'
              AND STREAM_SCHEMA = 'SADB'
              AND STREAM_NAME = SPLIT_PART(rec.STREAM_NAME, '.', 3);
            
            v_has_pending_data := SYSTEM$STREAM_HAS_DATA(rec.STREAM_NAME);
            
            v_stream_status := CASE 
                WHEN v_is_stale = TRUE THEN 'STALE'
                WHEN v_hours_until_stale < 24 THEN 'WARNING'
                ELSE 'HEALTHY'
            END;
            
            INSERT INTO D_BRONZE.CDC_MONITORING.CDC_STREAM_HEALTH_SNAPSHOT 
                (SNAPSHOT_TMS, TABLE_NAME, STREAM_NAME, IS_STALE, STALE_AFTER, 
                 HOURS_UNTIL_STALE, HAS_PENDING_DATA, STREAM_STATUS)
            VALUES 
                (:v_snapshot_tms, :v_table_name, :v_stream_name, :v_is_stale, 
                 :v_stale_after, :v_hours_until_stale, :v_has_pending_data, :v_stream_status);
            
            v_count := v_count + 1;
        EXCEPTION
            WHEN OTHER THEN
                INSERT INTO D_BRONZE.CDC_MONITORING.CDC_STREAM_HEALTH_SNAPSHOT 
                    (SNAPSHOT_TMS, TABLE_NAME, STREAM_NAME, IS_STALE, STREAM_STATUS)
                VALUES 
                    (:v_snapshot_tms, :v_table_name, :v_stream_name, NULL, 'ERROR');
        END;
    END FOR;
    CLOSE c1;
    
    v_result := 'Stream health captured for ' || v_count || ' streams at ' || TO_VARCHAR(:v_snapshot_tms);
    RETURN v_result;
EXCEPTION
    WHEN OTHER THEN
        RETURN 'ERROR: ' || SQLERRM;
END;
$$;

-- =============================================================================
-- STEP 9: CREATE STORED PROCEDURE - CAPTURE TASK HEALTH (FIXED - USING ACCOUNT_USAGE)
-- =============================================================================
CREATE OR REPLACE PROCEDURE D_BRONZE.CDC_MONITORING.SP_CAPTURE_TASK_HEALTH()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS CALLER
AS
$$
DECLARE
    v_result VARCHAR;
    v_snapshot_tms TIMESTAMP_NTZ := CURRENT_TIMESTAMP();
    v_count NUMBER := 0;
BEGIN
    INSERT INTO D_BRONZE.CDC_MONITORING.CDC_TASK_HEALTH_SNAPSHOT 
        (SNAPSHOT_TMS, TABLE_NAME, TASK_NAME, TASK_STATE, LAST_RUN_TMS, LAST_RUN_STATUS, 
         LAST_RUN_DURATION_SEC, MINUTES_SINCE_LAST_RUN, IS_HEALTHY)
    SELECT 
        :v_snapshot_tms,
        c.TABLE_NAME,
        c.TASK_NAME,
        th.STATE,
        th.COMPLETED_TIME,
        th.STATE,
        TIMESTAMPDIFF(SECOND, th.SCHEDULED_TIME, th.COMPLETED_TIME),
        TIMESTAMPDIFF(MINUTE, th.COMPLETED_TIME, CURRENT_TIMESTAMP()),
        CASE 
            WHEN th.STATE = 'SUCCEEDED' AND TIMESTAMPDIFF(MINUTE, th.COMPLETED_TIME, CURRENT_TIMESTAMP()) < c.ALERT_THRESHOLD_MINUTES THEN TRUE
            ELSE FALSE
        END
    FROM D_BRONZE.CDC_MONITORING.CDC_PIPELINE_CONFIG c
    LEFT JOIN (
        SELECT 
            DATABASE_NAME,
            SCHEMA_NAME,
            NAME,
            STATE,
            SCHEDULED_TIME,
            COMPLETED_TIME,
            ROW_NUMBER() OVER (PARTITION BY DATABASE_NAME, SCHEMA_NAME, NAME ORDER BY COMPLETED_TIME DESC) AS RN
        FROM SNOWFLAKE.ACCOUNT_USAGE.TASK_HISTORY
        WHERE DATABASE_NAME = 'D_RAW' 
          AND SCHEMA_NAME = 'SADB'
          AND SCHEDULED_TIME >= DATEADD(HOUR, -24, CURRENT_TIMESTAMP())
    ) th ON th.DATABASE_NAME || '.' || th.SCHEMA_NAME || '.' || th.NAME = c.TASK_NAME
        AND th.RN = 1
    WHERE c.IS_ACTIVE = TRUE;
    
    v_count := SQLROWCOUNT;
    v_result := 'Task health captured for ' || v_count || ' tasks at ' || TO_VARCHAR(:v_snapshot_tms);
    RETURN v_result;
EXCEPTION
    WHEN OTHER THEN
        RETURN 'ERROR: ' || SQLERRM;
END;
$$;

-- =============================================================================
-- STEP 10: CREATE STORED PROCEDURE - CAPTURE DATA QUALITY METRICS
-- =============================================================================
CREATE OR REPLACE PROCEDURE D_BRONZE.CDC_MONITORING.SP_CAPTURE_DATA_QUALITY_METRICS()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS CALLER
AS
$$
DECLARE
    v_result VARCHAR;
    v_snapshot_tms TIMESTAMP_NTZ := CURRENT_TIMESTAMP();
    v_table_name VARCHAR;
    v_source_count NUMBER;
    v_target_count NUMBER;
    v_deleted_count NUMBER;
    v_latest_source TIMESTAMP_NTZ;
    v_latest_target TIMESTAMP_NTZ;
    v_sql VARCHAR;
    v_count NUMBER := 0;
    c1 CURSOR FOR 
        SELECT TABLE_NAME 
        FROM D_BRONZE.CDC_MONITORING.CDC_PIPELINE_CONFIG 
        WHERE IS_ACTIVE = TRUE;
BEGIN
    OPEN c1;
    FOR rec IN c1 DO
        v_table_name := rec.TABLE_NAME;
        
        BEGIN
            v_sql := 'SELECT COUNT(*) FROM D_RAW.SADB.' || v_table_name || '_BASE';
            EXECUTE IMMEDIATE v_sql INTO v_source_count;
            
            v_sql := 'SELECT COUNT(*), SUM(CASE WHEN IS_DELETED THEN 1 ELSE 0 END) FROM D_BRONZE.SADB.' || v_table_name;
            EXECUTE IMMEDIATE v_sql INTO v_target_count, v_deleted_count;
            
            v_sql := 'SELECT MAX(SNW_LAST_REPLICATED) FROM D_RAW.SADB.' || v_table_name || '_BASE';
            EXECUTE IMMEDIATE v_sql INTO v_latest_source;
            
            v_sql := 'SELECT MAX(RECORD_UPDATED_AT) FROM D_BRONZE.SADB.' || v_table_name;
            EXECUTE IMMEDIATE v_sql INTO v_latest_target;
            
            INSERT INTO D_BRONZE.CDC_MONITORING.CDC_DATA_QUALITY_METRICS (
                SNAPSHOT_TMS, TABLE_NAME, SOURCE_ROW_COUNT, TARGET_ROW_COUNT, 
                ROW_COUNT_DIFF, ROW_COUNT_MATCH_PCT, DELETED_RECORDS_COUNT,
                LATEST_SOURCE_UPDATE, LATEST_TARGET_UPDATE, UPDATE_LAG_MINUTES,
                DATA_QUALITY_STATUS
            )
            VALUES (
                :v_snapshot_tms,
                :v_table_name,
                :v_source_count,
                :v_target_count,
                :v_target_count - :v_source_count,
                CASE WHEN :v_source_count > 0 
                     THEN ROUND((:v_target_count - COALESCE(:v_deleted_count, 0)) / :v_source_count * 100, 2) 
                     ELSE 100 END,
                COALESCE(:v_deleted_count, 0),
                :v_latest_source,
                :v_latest_target,
                TIMESTAMPDIFF(MINUTE, :v_latest_source, :v_latest_target),
                CASE 
                    WHEN ABS(:v_target_count - :v_source_count) <= 10 THEN 'HEALTHY'
                    WHEN ABS(:v_target_count - :v_source_count) <= 100 THEN 'WARNING'
                    ELSE 'CRITICAL'
                END
            );
            v_count := v_count + 1;
        EXCEPTION
            WHEN OTHER THEN
                INSERT INTO D_BRONZE.CDC_MONITORING.CDC_DATA_QUALITY_METRICS (
                    SNAPSHOT_TMS, TABLE_NAME, DATA_QUALITY_STATUS
                ) VALUES (:v_snapshot_tms, :v_table_name, 'ERROR');
        END;
    END FOR;
    CLOSE c1;
    
    v_result := 'Data quality metrics captured for ' || v_count || ' tables at ' || TO_VARCHAR(:v_snapshot_tms);
    RETURN v_result;
EXCEPTION
    WHEN OTHER THEN
        RETURN 'ERROR: ' || SQLERRM;
END;
$$;

