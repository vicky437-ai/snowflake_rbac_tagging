CREATE OR REPLACE PROCEDURE D_BRONZE.CDC_MONITORING.SP_ACKNOWLEDGE_ALERT(
    P_ALERT_ID NUMBER, P_ACKNOWLEDGED_BY VARCHAR, P_RESOLUTION_NOTES VARCHAR DEFAULT NULL
)
RETURNS VARCHAR LANGUAGE SQL EXECUTE AS CALLER
AS
$$
BEGIN
    UPDATE D_BRONZE.CDC_MONITORING.CDC_ALERT_LOG
    SET IS_ACKNOWLEDGED = TRUE, ACKNOWLEDGED_BY = P_ACKNOWLEDGED_BY,
        ACKNOWLEDGED_TMS = CURRENT_TIMESTAMP(), RESOLUTION_NOTES = P_RESOLUTION_NOTES
    WHERE ALERT_ID = P_ALERT_ID;
    IF (SQLROWCOUNT > 0) THEN RETURN 'Alert ' || P_ALERT_ID || ' acknowledged'; 
    ELSE RETURN 'Alert ' || P_ALERT_ID || ' not found'; END IF;
EXCEPTION WHEN OTHER THEN RETURN 'ERROR: ' || SQLERRM;
END;
$$;

CREATE OR REPLACE PROCEDURE D_BRONZE.CDC_MONITORING.SP_CLEANUP_OLD_MONITORING_DATA(P_RETENTION_DAYS NUMBER DEFAULT 90)
RETURNS VARCHAR LANGUAGE SQL EXECUTE AS CALLER
AS
$$
DECLARE
    v_cutoff_date TIMESTAMP_NTZ;
    v_deleted_stream NUMBER := 0; v_deleted_task NUMBER := 0; v_deleted_quality NUMBER := 0; v_deleted_alerts NUMBER := 0;
BEGIN
    v_cutoff_date := DATEADD(DAY, -P_RETENTION_DAYS, CURRENT_TIMESTAMP());
    DELETE FROM D_BRONZE.CDC_MONITORING.CDC_STREAM_HEALTH_SNAPSHOT WHERE CREATED_AT < :v_cutoff_date; v_deleted_stream := SQLROWCOUNT;
    DELETE FROM D_BRONZE.CDC_MONITORING.CDC_TASK_HEALTH_SNAPSHOT WHERE CREATED_AT < :v_cutoff_date; v_deleted_task := SQLROWCOUNT;
    DELETE FROM D_BRONZE.CDC_MONITORING.CDC_DATA_QUALITY_METRICS WHERE CREATED_AT < :v_cutoff_date; v_deleted_quality := SQLROWCOUNT;
    DELETE FROM D_BRONZE.CDC_MONITORING.CDC_ALERT_LOG WHERE CREATED_AT < :v_cutoff_date AND IS_ACKNOWLEDGED = TRUE; v_deleted_alerts := SQLROWCOUNT;
    RETURN 'Cleanup completed. Deleted: Stream=' || v_deleted_stream || ', Task=' || v_deleted_task || ', Quality=' || v_deleted_quality || ', Alerts=' || v_deleted_alerts;
EXCEPTION WHEN OTHER THEN RETURN 'ERROR: ' || SQLERRM;
END;
$$;

